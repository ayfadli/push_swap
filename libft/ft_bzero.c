#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*tmp_str;
	size_t	i;

	i = 0;
	tmp_str = (char *)s;
	while (i < n)
	{
		*tmp_str++ = 0;
		i++;
	}
}
